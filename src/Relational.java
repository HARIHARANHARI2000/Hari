
public class Relational{
	
	public static void main(String[] args) {
		int a=20,b=5,c=2;
		System.out.println(a>b&&a>c);
		System.out.println(b>a||b>c);
		System.out.println(!(a>b));
		a+=2;
		System.out.println("a+="+a);
		b-=10;
		System.out.println("b-="+b);
		a*=3;
		System.out.println("a*="+a);
		c/=2;
		System.out.println("c/="+c);

	}

}
